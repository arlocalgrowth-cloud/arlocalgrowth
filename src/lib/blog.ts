export function formatDate(dateStr: string, locale: string): string {
  const date = new Date(dateStr);
  return date.toLocaleDateString(
    locale === "ru" ? "ru-RU" : locale === "en" ? "en-GB" : "de-DE",
    { day: "numeric", month: "long", year: "numeric" }
  );
}
